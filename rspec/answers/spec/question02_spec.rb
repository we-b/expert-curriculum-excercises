describe '四則演算' do
  describe '足し算' do
    it '3 + 5 は 8 になること' do
      expect(3 + 5).to eq 8
    end
  end

  describe '引き算' do
    it '10 - 1 は 9 になること' do
      expect(10 - 1).to eq 9
    end
  end

  describe '掛け算' do
    it '5 * 4 は 20 になること' do
      expect(5 * 4).to eq 20
    end
  end

  describe '割り算' do
    it '40 / 8 は 5 になること' do
      expect(40 / 8).to eq 5
    end
  end
end
